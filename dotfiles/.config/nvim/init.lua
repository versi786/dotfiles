    --   end,
    -- },

    -- Testing
    ["<c-q>"] = cmp.mapping.confirm {
      behavior = cmp.ConfirmBehavior.Replace,
      select = false,
    },

    -- If you want tab completion :'(
    --  First you have to just promise to read `:help ins-completion`.
